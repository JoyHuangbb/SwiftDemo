import UIKit

extension UIFont{
    //MARK: - 统一设置字体，方便后期字体维护
    class func font10() ->UIFont {
        return UIFont.systemFont(ofSize: 10)
    }
    
    class func font11() ->UIFont {
        return UIFont.systemFont(ofSize: 11)
    }
    
    class func font12() ->UIFont {
        return UIFont.systemFont(ofSize: 12)
    }
    
    class func font13() ->UIFont {
        return UIFont.systemFont(ofSize: 13)
    }
    
    class func font14() ->UIFont {
        return UIFont.systemFont(ofSize: 14)
    }
    
    class func font15() ->UIFont {
        return UIFont.systemFont(ofSize: 15)
    }
    
    class func font16() ->UIFont {
        return UIFont.systemFont(ofSize: 16)
    }
    
    class func font17() ->UIFont {
        return UIFont.systemFont(ofSize: 17)
    }
    
    class func font18() ->UIFont {
        return UIFont.systemFont(ofSize: 18)
    }
    
}
